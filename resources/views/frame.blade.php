                    <h1 class="big-text">What are the benefits?</h1>

                    <p>Music therapists use music as a therapeutic tool to assess and improve emotional well-being, physical health, social functioning, communication abilities, and cognitive skills through musical responses; design music sessions for individuals and groups based on client needs using music improvisation, receptive music listening, song writing, lyric discussion, music and imagery, music performance, and learning through music; participate in interdisciplinary treatment planning, ongoing evaluation, and follow up. 
                    </p>
                    <h3>Examples</h3>
                    <ul>
                        <li>Working with children who have autism to improve communication capabilities.</li>
                        <li>Working with premature infants to improve sleep patterns and increase weight gain.</li>
                        <li>Working with people who have Parkinson’s disease to improve motor function.</li>
                        <li>Working with older adults to lessen the effects of dementia.
                        </li>
                        <li>Working with hospitalized patients to reduce pain.
                        </li>
                        <li>Working with stroke patients to help regain speech and motor function
